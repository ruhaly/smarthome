package com.changhong.smarthome.phone.cinema.entry;

import java.io.Serializable;

public class MovieSource implements Serializable {
	private String sourcePicUrl;
	private int id;
	private String name;
    public String getSourcePicUrl()
    {
        return sourcePicUrl;
    }
    public void setSourcePicUrl(String sourcePicUrl)
    {
        this.sourcePicUrl = sourcePicUrl;
    }
    public int getId()
    {
        return id;
    }
    public void setId(int id)
    {
        this.id = id;
    }
    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }

    
	
	
	
}
